package Ficha03;

public class Ex_06 {

    public static void main(String[] args) {

        int c=1,somador=0;

        while (c<=100){
            System.out.println(c);
            somador=somador+c;
            c++;
        }
        System.out.println("O somatório destes valores é: "+ somador);
    }

}
